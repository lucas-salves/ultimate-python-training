## Some of the built-in methods that modify strings:

## Upper case
a = "Olá, Mundo!!";
print(a.upper())
## Lower case
print(a.lower())

## Remove whitespace from the beginning or the end of the string
print(a.strip())

## Replace String
print(a.replace("O","J"))

## Split String
print(a.split(",")) # returns ['Olá','Mundo!']
