# Variables can even change the type after they have been set.

x = 3
x = "Sally"
print(x)

# Specifying the data type of a variable:

x = str(3) # x will be '3'
y = int(3) # y will be 3
z = float(3) # z will be 3.0

print(type(x), type(y), type(z))

